
#include<iostream>
using namespace std;

/**
-------------------------Shifting---------------
<<  - left shift

>> - right shift

2 << 1 = 00000000000000000000000000000010 << 1
         00000000000000000000000000000100 = 4

5 << 2 = 00000000000000000000000000000101 << 2
         00000000000000000000000000010100 = 20

         Left shift shortcut
         a << n = (int)(a * pow(2,n))

3 >> 1 = 00000000000000000000000000000011 >> 1
         00000000000000000000000000000001 = 1

5 >> 2 = 00000000000000000000000000000101 >> 2
         00000000000000000000000000000001 = 1

7 >> 1 = 00000000000000000000000000000111 >> 1
         00000000000000000000000000000011 = 3

         Right shift shortcut
         a >> n = (int)(a / pow(2,n))
*/

int main()
{
    cout<<(2<<1)<<endl;
    cout<<(5<<2)<<endl;
    cout<<(7<<2)<<endl;
    cout<<(3>>1)<<endl;
    cout<<(5>>2)<<endl;
    cout<<(7>>1)<<endl;
    cout<<(25>>2)<<endl;
}
